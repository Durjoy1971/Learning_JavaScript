let grade = "A";

switch (grade) {
  case "A":
    console.log("You did Great");
    break;
  case "B":
    console.log("You did Well");
    break;
  case "C":
    console.log("You did Ok");
    break;
  case "D":
    console.log("You Passed ");
    break;
  case "F":
    console.log("You Failed");
    break;
  default:
    console.log(grade, "is not a letter grade")
}
